#include "ft_printf.h"

int	ft_putstr_fd(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	if (s == NULL)
	{
		ft_putstr_fd("(null)");
		return (6);
	}
	return (i);
}
